class  Alphabet
{
	public static void main(String[] args) 
	{
		int i,j;
		String s1[]={"Apple","Banana","Orange","Litchi","Mango"};
		String s2[]={"Red","Yello","Orange","Pink","Green"};
		
		for(i=0;i<6;i=i+1)
		{
			System.out.println(s1[i]);
		}
		for(j=2;j<6;j=j+2)
		{
			System.out.println(s2[j]);
		}
	}
}
