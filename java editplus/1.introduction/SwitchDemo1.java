//this demo is for switch statements
class SwitchDemo1 
{
	public static void main(String[] args) 
	{
		int var=10;
		switch(var)
		{
			case 10:
				System.out.println("Hello");
			break;
			case 20:
				System.out.println("Hi");
			break;
			default:
				System.out.println("your choice is decent");
		}
		System.out.println("out of switch block");
	}
}
