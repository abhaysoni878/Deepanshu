package RAJSONI;

public class ExampleOfLoops {
static void add(int a,int b) {
	System.out.println(a+b);
}

public static void main(String[] args) {
	
	add(10, 20);
	sub(10,20);
	test1(10,10);
}
static void sub(int a,int b) {
	System.out.println(a-b);
}
static void test1(int a,int b) {
	a++;
	b--;
	System.out.println(a*b);
	
	
}

}
